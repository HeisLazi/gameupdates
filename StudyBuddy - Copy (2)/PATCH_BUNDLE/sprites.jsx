/* Pixel sprite system — every glyph is hand-drawn pixel art via inline SVG <rect>s.
   Sprites use a 16x16 grid; colors come from props or currentColor. */

const PX = (x,y,c='currentColor') => <rect x={x} y={y} width="1" height="1" fill={c} key={x+','+y} />;

function pix(rows, palette){
  // rows: array of strings, each char is a palette key (' ' = transparent)
  const out = [];
  for (let y=0; y<rows.length; y++){
    for (let x=0; x<rows[y].length; x++){
      const ch = rows[y][x];
      if (ch === ' ' || ch === '.') continue;
      const c = palette[ch] || 'currentColor';
      out.push(<rect x={x} y={y} width="1" height="1" fill={c} key={x+','+y} />);
    }
  }
  return out;
}

const Sprite = ({ rows, palette, viewBox='0 0 16 16', className='', style }) => (
  <svg viewBox={viewBox} className={className} style={style} shapeRendering="crispEdges">
    {pix(rows, palette)}
  </svg>
);

